class DataTypesExample {
    public static void main(String[] args) {
        int a = 10;
        double b = 12.5;
        char c = 'A';
        boolean d = true;
        String e = "Java";

        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Character: " + c);
        System.out.println("Boolean: " + d);
        System.out.println("String: " + e);
    }
}
